#!/usr/bin/env node
/**
 * Regenerates `components/previews/index.tsx` — the preview map the
 * registry-shell reads to render each component's Preview tab.
 *
 * Re-run after adding or removing components from registry.json.
 */
import fs from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

const HERE = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(HERE, "..")
const OUT = path.join(ROOT, "components", "previews", "index.tsx")

function pascalCase(slug) {
  return slug
    .split("-")
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("")
}

async function main() {
  const registry = JSON.parse(
    await fs.readFile(path.join(ROOT, "registry.json"), "utf-8"),
  )
  const components = (registry.items ?? []).filter(
    (i) =>
      i.type === "registry:ui" ||
      i.type === "registry:component" ||
      i.type === "registry:block",
  )

  const entries = components.map((i) => {
    const Pascal = pascalCase(i.name)
    return `  "${i.name}": dynamic(() => import("./${i.name}-preview").then((m) => m.${Pascal}Preview)),`
  })

  const content = `/**
 * Preview map auto-generated by scripts/generate-preview-index.mjs.
 * Regenerate after adding/removing components from registry.json.
 *
 *   node scripts/generate-preview-index.mjs
 */
import type { ComponentType, ReactNode } from "react"
import dynamic from "next/dynamic"
import type { PreviewLoader } from "@sntlr/registry-shell/shell/lib/registry-adapter"

const map: Record<string, ComponentType> = {
${entries.join("\n")}
}

export const previewLoader: PreviewLoader = {
  Preview: ({ name, fallback }: { name: string; fallback?: ReactNode }) => {
    const Component = map[name]
    if (!Component) return fallback ?? null
    return <Component />
  },
  load: (name: string) => map[name] ?? null,
  names: () => Object.keys(map),
}
`

  await fs.writeFile(OUT, content, "utf-8")
  console.log(`✓ wrote ${OUT} with ${components.length} entries`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
